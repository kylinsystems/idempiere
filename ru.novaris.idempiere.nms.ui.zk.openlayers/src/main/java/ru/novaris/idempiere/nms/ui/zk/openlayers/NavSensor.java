package ru.novaris.idempiere.nms.ui.zk.openlayers;

public class NavSensor
{
	private long	nmSensor;
	private String	nmName;
	private String	sensorKey;
	private long	nmSensorType;
	private Long	nmModule;

	public NavSensor()
	{

	}

	public long getNmSensor()
	{
		return nmSensor;
	}

	public void setNmSensor(long nmSensor)
	{
		this.nmSensor = nmSensor;
	}

	public String getNmName()
	{
		return nmName;
	}

	public void setNmName(String nmName)
	{
		this.nmName = nmName;
	}

	public String getSensorKey()
	{
		return sensorKey;
	}

	public void setSensorKey(String sensorKey)
	{
		this.sensorKey = sensorKey;
	}

	public long getNmSensorType()
	{
		return nmSensorType;
	}

	public void setNmSensorType(long nmSensorType)
	{
		this.nmSensorType = nmSensorType;
	}

	public Long getNmModule()
	{
		return nmModule;
	}

	public void setNmModule(Long nmModule)
	{
		this.nmModule = nmModule;
	}

}
